package classDesignStoringAssignmentResults;

public class StudentScore {
	private String name;
	private int score;
	
	public StudentScore(String name, int score) {
		this.name = name;
		this.score = score;
	}
	
	public String getName() {
		return name;
	}
	
	public int getScore() {
		return score;
	}
}
 